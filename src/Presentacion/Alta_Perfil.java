    private boolean controlDatos() throws Exception {

        if (jT_nick.getText() == null) {
            JOptionPane.showMessageDialog(null, "Nickname vacio");
            jT_nick.selectAll();
            jT_nick.requestFocus();
            return false;
        }
        if (contUsu.existeUsuario(jT_nick.getText())) {
            JOptionPane.showMessageDialog(null, "Ya existe un otro usuario con este nickname");
            jT_nick.selectAll();
            jT_nick.requestFocus();
            return false;
        }
        if (jP_contra.getText() == null) {
            JOptionPane.showMessageDialog(null, "Debe ingresar una contrase침a");
            jP_contra.selectAll();
            jP_contra.requestFocus();
            return false;
        }
        if (jP_contraVS.getText() == null) {
            JOptionPane.showMessageDialog(null, "Debe ingresar la confirmacion de la contrase침a");
            jP_contraVS.selectAll();
            jP_contraVS.requestFocus();
            return false;
        }

        if (jP_contra.getText().equals(jP_contraVS.getText()) != true) {
            JOptionPane.showMessageDialog(null, "Contrase침as diferentes!");
            jP_contra.selectAll();
            jP_contra.requestFocus();
            return false;
        }
        if (jT_nombre.getText() == null) {
            JOptionPane.showMessageDialog(null, "Nombre vacio");
            jT_nombre.selectAll();
            jT_nombre.requestFocus();
            return false;
        }
        if (jT_apellido.getText() == null) {
            JOptionPane.showMessageDialog(null, "Apellido vacio");
            jT_apellido.selectAll();
            jT_apellido.requestFocus();
            return false;
        }
        if (jT_email.getText() == null) {
            JOptionPane.showMessageDialog(null, "Email vacio");
            jT_email.selectAll();
            jT_email.requestFocus();
            return false;
        }
        if (compruebaEmail(jT_email.getText()) != true) {
            JOptionPane.showMessageDialog(null, "Email incorrecto");
            jT_email.selectAll();
            jT_email.requestFocus();
            return false;
        }
        if (contUsu.existeMail(jT_email.getText())) {
            JOptionPane.showMessageDialog(null, "Ya existe otro usuario registrado con este mail");
            jT_email.selectAll();
            jT_email.requestFocus();
            return false;
        }
        if (jdc_fechaNac == null) {
            JOptionPane.showMessageDialog(null, "Fecha nacimiento vacia");
            jdc_fechaNac.requestFocus();
            return false;
        }
        return true;
    }

    private boolean fechaok() {
        int dia = 0, mes = 0, anio = 0;
        dia = jdc_fechaNac.getDate().getDay();
        mes = jdc_fechaNac.getDate().getMonth();
        anio = jdc_fechaNac.getDate().getYear();

        int Cdia = 01, cMes = 01, Canio = 1945;
        if (dia == Cdia) {
            if (mes == cMes) {
                if (anio == Canio) {
                    return true;
                }
            }
        }
        return false;
    }

    private dtFecha getFechajdc() {
        dtFecha fecha;
        fecha = new dtFecha(Integer.toString(jdc_fechaNac.getDate().getDay()), Integer.toString(jdc_fechaNac.getDate().getMonth()), Integer.toString(jdc_fechaNac.getDate().getYear()));
        return fecha;
    }

    private boolean compruebaEmail(String email) {
        // Patr칩n para validar el email
        Pattern pattern = Pattern.compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
        Matcher mather = pattern.matcher(email);

        return mather.find();
    }

